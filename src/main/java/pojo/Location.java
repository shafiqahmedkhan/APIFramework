package pojo;

public class Location {
    private double lat;
    private double lan;

    public double getLat(double v) {
        return lat;
    }

    public void setLat(double lat) {
        this.lat = lat;
    }

    public double getLan() {
        return lan;
    }

    public void setLan(double lan) {
        this.lan = lan;
    }
}
